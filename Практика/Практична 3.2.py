'''11.	Дані дійсні числа x, y. якщо x та y від’ємні, то х призначити модуль х.
якщо від’ємне одне з них, то збільшити у на 0.5.
Якщо обидва числа додатні, то збільшити х в 10 раз.
'''
import math

x = float(input('Введіть значення x: '))
y = float(input('Введіть значення y: '))

if x < 0 and y < 0:
    x = math.fabs(x)
if x > 0 and y < 0 or x < 0 and y > 0:
    y += 0.5
if x > 0 and y > 0:
    x *= 10

print('Значення x: ', x)
print('Значення y: ', y)
    
