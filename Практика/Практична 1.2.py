'''11.	Знайдіть площу кільця, внутрішній радіус якого дорівнює 20,
зовнішній радіус являє собою задане число R > 20.'''
Ra = 20
R = int(input('Введіть значення зовнішнього радіуса (>20) '))
while R <= 20:
    R = int(input('Ви невірно ввели значення, спробуйте ще раз '))
print('Площа кільця ', 3.14*(R**2 - Ra**2))
